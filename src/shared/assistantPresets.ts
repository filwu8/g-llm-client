export function searchAssistantPresets(keyword: string, category = '精选'): AssistantPreset[] {
  const text = keyword.trim().toLowerCase()
  const useFeatured = category === '精选'

  return GLLM_ASSISTANT_PRESETS.filter((preset) => {
    if (useFeatured && !preset.featured) return false
    if (!useFeatured && category && preset.category !== category) return false
    if (!text) return true

    const content = [preset.name, preset.title, preset.description, preset.category, ...preset.keywords]
      .join('\n')
      .toLowerCase()

    return content.includes(text) || preset.keywords.some((keywordItem) => text.includes(keywordItem.toLowerCase()))
  })
}

export function findAssistantPreset(keyword: string): AssistantPreset | null {
  const text = keyword.trim().toLowerCase()
  if (!text) return null

  return (
    GLLM_ASSISTANT_PRESETS.find((preset) => preset.name.toLowerCase() === text) ??
    GLLM_ASSISTANT_PRESETS.find((preset) =>
      [preset.name, preset.title, preset.description, preset.category, ...preset.keywords]
        .join('\n')
        .toLowerCase()
        .includes(text)
    ) ??
    null
  )
}
